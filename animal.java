package atv_animal;

public class animal {
    private String id;
    private String tipo;
    private String habitat;

    public String getid(){
        return id;
    }

    public void setid(String id){
        this.id = id;
    }

    public String gettipo(){
        return tipo;
    }

    public void settipo(String tipo){
        this.tipo = tipo;
    }

    public String gethabitat(){
        return habitat;
    }

    public void sethabitat(String habitat){
        this.habitat = habitat;
    }
}
